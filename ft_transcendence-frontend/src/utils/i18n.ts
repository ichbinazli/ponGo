export class I18n {
    private static instance: I18n;
    private currentLanguage: Language;

    private constructor() {
        this.currentLanguage = this.loadLanguage();
    }

    public static getInstance(): I18n {
        if (!I18n.instance) {
            I18n.instance = new I18n();
        }
        return I18n.instance;
    }

    public t(key: string): string {
        return translations[this.currentLanguage]?.[key]
            ?? translations['tr']?.[key]
            ?? key;
    }

    public getLanguage(): Language {
        return this.currentLanguage;
    }

    public setLanguage(lang: Language): void {
        this.currentLanguage = lang;
        localStorage.setItem('language', lang);
        this.applyTranslations();
    }

    private loadLanguage(): Language {
        const saved = localStorage.getItem('language') as Language | null;
        if (saved && translations[saved]) {
            return saved;
        }
        return 'tr';
    }

    public applyTranslations(): void {
        document.querySelectorAll('[data-i18n]').forEach((el) => {
            const key = el.getAttribute('data-i18n');
            if (key) {
                el.textContent = this.t(key);
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (key) {
                (el as HTMLInputElement).placeholder = this.t(key);
            }
        });

        document.querySelectorAll('[data-i18n-title]').forEach((el) => {
            const key = el.getAttribute('data-i18n-title');
            if (key) {
                (el as HTMLElement).title = this.t(key);
            }
        });

        const langSelect = document.getElementById('language-select') as HTMLSelectElement | null;
        if (langSelect && langSelect.value !== this.currentLanguage) {
            langSelect.value = this.currentLanguage;
        }

        document.documentElement.lang = this.currentLanguage;
    }
}
